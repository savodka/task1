package CalculatorOop;

public class Calculation {
    public float plus(float a, float b) {
        return a + b;
    }

    public float multiply(float a, float b) {
        return a * b;
    }

    public float subtract(float a, float b) {
        return a - b;
    }

    public float divide(float a, float b) {
        return a / b;
    }
}